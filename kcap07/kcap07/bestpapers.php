<h1>Best Papers</h1>
There were two best paper awards presented at the opening session on Monday morning, for the best technical paper, and the best student paper.

<a name="btp"><h2>Best Technical Paper</h2>
The winner of the best technical paper award for K-CAP 2007 is <span class="papername">Interactive Thesaurus Assessment for Automatic Document Annotation</span> authored by
<span class="authornames">Kai Eckert, Heiner Stuckenschmidt, and Magnus Pfeffer</span>.

<h3>Best Paper Shortlist</h3>
The following papers were also shortlisted for the prize: <br />

<ul>
	<li>
	<span class="papername">The X-COSIM Integration Framework for a Seamless Semantic Desktop</span> <br />
	<span class="authornames">Thomas Franz, Steffen Staab, Richard Arndt</span> <br />
	</li>
	<li>
	<span class="papername">Maintaining Constraint-based Applications</span> <br />
	<span class="authornames">Tomas Eric Nordlander, Eugene C. Freuder, Richard J. Wallace</span> <br />
	</li>
	<li>
	<span class="papername">Enabling Experts to Build Knowledge Bases from Science Textbooks</span> <br />
	<span class="authornames">Vinay K. Chaudhri, Bonnie E. John, Sunil Mishra, John Pacheco, Bruce Porter, Aaron Spaulding</span> <br />
	</li>
</ul>

<a name="bsp"><h2>Best Student Paper</h2>
The winner of the best student paper for K-CAP 2007 is <span class="papername">Strategies for Lifelong Knowledge Extraction from the Web</span> by
<span class="authornames">Michele Banko and Oren Etzioni</span>.

<h3>Best Student Paper Shortlist</h3>
The following papers were also shortlisted for the prize: <br />
<ul>
	<li>
	<span class="papername">Evaluation of a Temporal-Abstraction Knowledge Acquisition Tool in the Network Security Domain</span> <br />
	<span class="authornames">Asaf Shabtai, Maor Atlas, Yuval Shahar, Yuval Elovici</span> <br />
	</li>
	<li>
	<span class="papername">Information Acquisition using Multiple Classifications</span> <br />
	<span class="authornames">Namhee Kwon, Eduard Hovy</span> <br />
	</li>
	<li>
	<span class="papername">KBS Development through Ontology Mapping and Ontology Driven Acquisition</span> <br />
	<span class="authornames">David Corsar, Derek Sleeman</span> <br />
	</li>
</ul>



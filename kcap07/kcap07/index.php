<h1>EKAW 2008</h1>
The K-CAP conference series will continue in 2009; in the meantime we encourage authors interested in this area to consider submitting to <a href="http://ekaw2008.inrialpes.fr/">EKAW 2008</a>, which 
will be held from 29th September to 3rd October 2008 in Acitrezza, Catania, Italy.  Further information: <a href="http://ekaw2008.inrialpes.fr/">vist the conference website</a>.



<h1>Thank You!</h1>
<p>K-CAP 2007 was a great success, with 71 people attending the main conference, and 56 attending the workshops. 
 A big thank you to all of those who attended the conference, and to those who presented their work, for helping make K-CAP 2007 one of the best yet!
</p>

<p>
Also a big thank you to all those involved in the organisation of K-CAP 2007, 
both the main conference and the four workshops, without your hard work K-CAP 2007 would not have been such a success!  
Similarly, we would like to thank all those on the program committee for all their efforts.
</p>

<p>
Finally, we would also like to thank our sponsors: <a href="http://sigart.acm.org/" rel="external">ACM SIGART</a>, <a href="http://www.aaai.org" rel="external">AAAI</a>, <a href="http://www.boeing.com/" rel="external">Boeing</a>,  <a href="http://www.vulcan.com" rel="external">Vulcan Inc.</a> and <a href="http://knowledgeweb.semanticweb.org" rel="external">Knowledge Web</a>.
</p> 

<p>
We look forward to seeing you all again at K-CAP 2009!

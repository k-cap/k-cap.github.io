<h1>About K-CAP 2007</h1>
<p>The Fourth International Conference on Knowledge Capture, K-CAP 2007 will feature invited Talks, Technical Papers, Posters and Demos on a range of topics including:</p>

<ul>
	<li>Knowledge Engineering &amp; modelling methodologies</li>
	<li>Knowledge Engineering &amp; the Semantic Web</li>
	<li>Mixed-initiative planning &amp; decision-support tools</li>
	<li>Acquisition of problem-solving knowledge</li>
	<li>Knowledge-based markup techniques</li>
	<li>Knowledge extraction systems</li>
	<li>Knowledge acquisition tools</li>
	<li>Advice taking systems</li>
</ul>



<p>The conference will be held at <a href="http://www.fairmont.com/whistler/" rel="external"><strong>The Fairmont Chateau Whistler</strong></a>.  
The main conference will be held on the <strong>29th to the 31st of October 2007</strong>, 
 with Workshops/Tutorials being held on the <strong>28th October 2007</strong>.
</p>


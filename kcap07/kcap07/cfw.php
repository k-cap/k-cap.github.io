<h1>K-CAP 2007: Call for Workshop Proposals</h1>

<h2> <span style="color:#ff0033">*Deadline Postponed*</span></h2>
<p>
*The submission deadline for workshop proposals has been recently postponed to <span style="color:#ff0033">April 20, 2007</span>.*
</p>

<p>
	The K-CAP 2007 Program Committee invites proposals for the Workshop
	Program for K-CAP 2007. Workshops will be held on Sunday, October 28th, 
	the day before the regular conference technical sessions begin, at the 
	conference hotel in Whistler, BC, Canada. 
</p>

<p>
	K-CAP workshops  provide an informal setting where workshop
	participants  have the opportunity to discuss specific technical
	topics in an atmosphere that fosters the active exchange of ideas. Our
	aim for the workshop program is to promote and collect
	multidisciplinary research directions that contribute to knowledge capture from a variety of
	sources. Members from all research areas related to knowledge capture
	are invited to submit workshop proposals.
	We particularly welcome workshops at discipline boundaries. 
</p>
<p>
	To encourage interaction and a broad exchange of ideas, each workshop
	will be limited to 30 participants and ample time will be allotted for
	general discussion. </p>

<p>
</p>

<h2>Important Dates for Workshops</h2>
<ul>
  <li><span style="color:#ff0033">April 20, 2007</span>: Proposal submission deadline
  </li>
  <li><span style="color:#ff0033">April 25, 2007</span>: Acceptance notification
  </li>
  <li>May 2, 2007: Publications of Workshops Call for Participation
  </li>
  <li>(Individual workshops may set submission deadlines as they see fit) </li>

  <li>Sept 10, 2007: Publication of K-CAP 2007 workshop program
  </li>
  <li>October 1, 2007: Deadline for camera-ready workshop notes and other information
  </li>
  <li>October 28, 2007: K-CAP 2007 workshops 
  </li>
</ul>

<h2>Requirements for Submission</h2>

<p>
	Proposals for workshops should be between two and five pages in length, and 
	should contain the following information: 
</p>
<ul>

	<li>Contact information of the proposed workshop chair (name,
	  department, university, telephone, fax and e-mail).
	</li><li>A brief technical description of the workshop, specifying the workshop
	  goals and the technical issues that will be its focus. 
	</li><li>A description of the workshop format, emphasizing how the format will
	  encourage discussion. 
	</li><li>A brief discussion of why and to whom the workshop is of interest. 
	</li><li>A description of the qualifications of the individual committee members
	  with respect to organizing the workshop, including a list of workshops
	  previously arranged by any members of the proposed organizing committee, if
	  any. 
	</li><li>A list of potential participants, ideally with an indication of
	  which participants have expressed an interest in attending the
	  workshop. 
	</li><li>A preliminary call for workshop papers. 
	</li></ul>

<h2>Timetable</h2>
<p>All proposals should be submitted by electronic mail, to
<a href="mailto:gennari@u.washington.edu?subject=K-CAP 2007 Workshop Proposal">gennari@u.washington.edu</a>, preferably in PDF (MS Word will also be accepted) by <span style="color:#ff0033">April 20, 2007</span>. 
</p>
<p> Prospective organizers will be notified of
      the committee's decision no later than <span style="color:#ff0033">April 25, 2007</span>. Each workshop
      organizing committee will be responsible for producing a Call for
      Participation for their workshop by May 2, 2007. By October 1, workshop organizing committees must provide any working notes to
      be duplicated for the workshops (up to a maximum of 150 pages), and a
      list of audio-visual requirements and any special room requirements.</p>
<h2>K-CAP will be responsible for the following: </h2>

<ul>
	<li>Providing publicity for the workshop series as a whole. 
	</li>
	<li>Providing logistics support and a meeting place for the workshop. 
	</li>
	<li>Duplicating working notes as described above and distributing them
	  to the participants.
	</li>
</ul>
<p>Please send your proposals and any inquiries to: 
</p>
<p>
John Gennari, <a href="mailto:gennari@u.washington.edu?subject=K-CAP 2007 Workshop Proposal">gennari@u.washington.edu</a>.</p>
<p>
Conference home page: <a href="http://www.csd.abdn.ac.uk/kcap07">http://www.csd.abdn.ac.uk/kcap07/</a>
</p>


            <td class="maincolumn" rowspan="2"><h1>Social Program</h1>
Delegates attending the main conference are invited to attend the following social events.

<h2>Welcome Dessert Reception</h2>

Delegates to the main conference are invited to attend the welcome dessert reception which will be held on the evening of Sunday 28th October, between 20:00 and 23:00, in 
Frontenac A.  There will be lots of dessert, so do not eat too much for dinner!  There will also be a host bar.


<h2>Conference Banquet</h2>
The conference banquet will be held on the evening of Monday 29th October, at <a href="http://www.ricsgrill.com">Ric’s Grill</a> Steak Seafood and Chop House, which is located at 4154 Village Green, in the lobby of the Crystal Lodge.

<h2>Poster Reception</h2>
The poster session will be held on Tuesday 30th October, in Frontenac C between 18:30 and 21:00  Lots of hor d'oeuvers will be available, as well as a host bar, during the session to enjoy while viewing the posters.

<h2>Extra Tickets</h2>
It is possible to purchase extra tickets for any of these events should you require.  Please catch David Corsar or Rob Kremer during the conference to make your purchase.


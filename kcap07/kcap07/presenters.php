<h1>Information for Presenters</h1>
To help you plan for your <a href="presenters.php#paper">presentation</a>/<a href="presenters.php#poster">poster</a>, please read the following information regarding what will be provided at the conference.


<a name="paper"></a><h2>Paper Presentations</h2>
The following information is relevant to those presenting a paper during the conference:
<ol>
<li>Paper presentations will take place in the Frontenac AB room.</li>
<li>Check the <a href="schedule.php">conference schedule</a> to see when you are presenting.</li>
<li>The slot for each paper presentation is 30 minutes long.  The session chair will ensure presenters do not overrun their allocated time.  Remember to leave time for questions! </li>
<li>It will be up to the session chair to organise the presentations before the session starts.  Please talk with the session chair during the break before your session regarding setting up for your presentation.  For more information, see the next point…</li>
<li>We will provide a laptop which should be used for presentations.  It is preferred that you use this for presentations to ensure each session runs smoothly.  This laptop will be running Windows XP, with at least PowerPoint 2000 and Adobe Acrobat Reader 7.0.  Presenters will be asked to transfer their presentation to this laptop via USB memory stick or CD before the start of the session by the session chair.  </li>
<li>If you are planning to give a demonstration or have other requirements for your presentation, you may be able to use your own laptop: this decision is up to the session chair.  </li>
<li>Do not count on having an Internet connection for any part of your presentation: we do now know if there will be a Internet connection available at the podium.  </li>
</ol>

<a name="poster"></a><h2>Posters</h2>
The following information is relevant to those presenting a poster:

<ol>
<li>The poster reception will be held from  18:30 until 20:30 in Frontenac C.</li>
<li>We will provide poster boards and pushpins. Each poster board will house
 two posters on each side (so you'll be sharing your side with one other
 poster). You have a square area of 48 inches by 48 inches to use to
 convey the message/results of your paper in an effective way to people
 browsing. The poster does not need to replicate the paper as submitted.
 You should also plan to have somebody manning the poster throughout the
 session to describe the work and answer questions.
</li>
<li>There is currently a one hour &quot;Review of Posters&quot; slot on the Tuesday (16:30 – 17:30).  During this session, you will have a maximum 3 minutes to give a summary of your poster and entice people to find out more later!</li>
<li>You will be able to access the room to set up your posters from 17:30 (between the Review of Posters and the Poster Reception).</li>
</ol>


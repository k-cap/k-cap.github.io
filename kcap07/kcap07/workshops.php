<table class="workshopTable" summary="Table summarising the Semantic Authoring, Annotation and Knowledge Markup workshop">
	<tr>
		<th>
			<a name="SAAKM"></a><a href="http://saakm2007.semanticauthoring.org/index.html" rel="external">Semantic Authoring, Annotation and Knowledge Markup (SAAKM)</a>
		</th>
	</tr>
	<tr>
		<td>
			<strong>Website: </strong> <a href="http://saakm2007.semanticauthoring.org/index.html" rel="external">http://saakm2007.semanticauthoring.org/</a><br />
			<strong>Organised by:</strong> Siegfried Handschuh<br />
			<strong>Length:</strong> Full Day<br />
			<strong>Summary: </strong> <br />
				<p>Capturing knowledge by using markup techniques and by supporting semantic
				annotations is a major technique for creating metadata. It is beneficial
				in a wide range of content-oriented intelligent applications.
				One important Application for instance is the Semantic Web. The research
				about the WWW currently strives to augment syntactic information
				already present in the Web by semantic metadata in order to achieve a Semantic
				Web that human and software agents can understand. Here, one of the most urgent
				challenges now is a knowledge-capturing problem, i.e. how one may turn existing
				syntactic resources into knowledge structures. A solution is to markup web
				document in order to create metadata on the web or to author new documents in
				a way that they contain markup directly.
				</p>
				<p>
				Another application is the indexing and searching of multimedia (and
				multilingual) data. It is difficult to completely process the content of
				multimedia data, even with technologies based on natural language processing,
				image processing, machine vision and speech recognition. Therefore, Semantic
				annotation is one of the promising methodologies to define semantic
				structures on the content. 
				</p>
				<p>
					Further details... <a href="http://saakm2007.semanticauthoring.org/index.html" rel="external">website</a>.
				</p>
		</td>

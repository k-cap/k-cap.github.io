<h1>Poster Submission Guidelines</h1>

<p>
Poster papers must be submitted electronically; the submission site <a href="postersubmit.php">is now available</a>.
</p>

<p>
Microsoft Word users please prepare your paper using the K-CAP 
<a href="http://www.csd.abdn.ac.uk/kcap07/docs/techpapersub/KCAP-paper-template.doc">Microsoft Word template</a>. LaTeX
users please use the K-CAP
<a href="http://www.csd.abdn.ac.uk/kcap07/docs/techpapersub/acm_proc_10ptArticle-sp.cls">class file</a>, 
<a href="http://www.csd.abdn.ac.uk/kcap07/docs/techpapersub/sigproc-KCAPsample.tex">sigproc-KCAPsample.tex example template file</a> (please follow the instructions contained within when submitting), <a href="http://www.csd.abdn.ac.uk/kcap07/docs/techpapersub/sigproc.bib">sigproc.bib example bibtex</a> file used by sigproc-KCAPsample.tex,  and <a href="http://www.csd.abdn.ac.uk/kcap07/docs/techpapersub/sigproc-KCAPsample.pdf">example pdf</a> of sigproc-KCAPsample.tex.
</p>

<p>
Papers must be no more than 2 unnumbered &quot;US-letter&quot; size pages 
(including references) using a 10-point Times Roman font for the main
body text. All material on each page should fit within a 
rectangle of 18x23.5 cm (7&quot;x9.25&quot;), centered on the page, beginning 
1.9 cm (.75&quot;) from the top of the page and ending with 2.54 cm (1&quot;) 
from the bottom. The right and left margins should be 1.9 cm (.75&quot;). 
The text should be in two 8.45 cm (3.33&quot;) columns with a .83 cm (.33&quot;) 
gutter. Specific instructions for titles, headings, graphics and
tables appear in the template files.
</p>

<p>
Papers that violate the guidelines may be rejected without review.
</p>

<p>
Authors will be required to transfer copyright of accepted papters to ACM.
</p>


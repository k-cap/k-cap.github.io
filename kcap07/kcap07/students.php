<h1>Information for Students</h1>
Students are encouraged to attend K-CAP 2007.  To assist students, K-CAP 2007 is offering <a href="students.php#bursaries">bursaries</a> to help with expenses, and will also require some <a href="students.php#assistants">student assistants</a> to help out during the conference.


<a name="bursaries"></a><h2>Student Bursaries</h2>
<p>
These are intended to contribute to the travel and living expenses for students participating in the K-CAP 2007 conference. We expect to allocate up to 6 such bursaries. First priority will be given to students who are co-authors of full papers (and preferably also presenting the paper at the conference). We also encourage students involved in posters and workshops to apply.
</p>
<p>
<strong>Application procedure:</strong> you should send an email by <strong>7 September</strong> to Derek Sleeman
(d.sleeman at abdn.ac.uk) outlining the  activities  you will be involved in at K-CAP 2007, and an outline of the expected total cost in attending K-CAP 2007. You should also provide the name of your supervisor, your thesis topic and the name of your department. By the same deadline you should ask your supervisor to email  Derek Sleeman, with a statement confirming he / she is your supervisor, and confirming you are a registered research student.
</p>

<a name="assistants"></a><h2>Student Assistants</h2>
<p>
We need up to 6 students to help us organize the workshops and conference; duties will involve helping with registration (to be coordinated by David Corsar), with ensuring sessions have appropriate audio-visual equipment, and that the K-CAP 2007 wireless network is functioning, and to generally support the organizing committee. In return for these activities you will be allocated (possibly shared) free accommodation for 4 nights (from 27-31 October) in a twin bedded room at the conference hotel.
</p>
<p>
<strong>Application procedure:</strong> you should send an email by <strong>7 September</strong> to Derek Sleeman
(d.sleeman at abdn.ac.uk) outlining what relevant experiences you might have, and an outline of your expected total cost to attend K-CAP 2007.  You should also provide the name of your supervisor, your thesis topic and the name of your department. By the same deadline you should ask your supervisor to email  Derek Sleeman, with a statement confirming he / she is your supervisor, and confirming you are a registered research student.
</p>


<h1>About K-CAP</h1>
<p>
Below is some information about the <a href="aboutkcap.php#aims">aims and topics</a> of K-CAP, 
along with some <a href="aboutkcap.php#history">history of the group and relevant research communities</a> and the <a href="aboutkcap.php#steering">steering committee</a>.  This information is also provided on the <a href="http://www.k-cap.org/k-cap/about-k-cap.html" rel="external">K-CAP website</a>.
</p>

<h2><a name="aims"></a>Aims and Topics of K-CAP</h2>
<p>
In today's Web-linked and data-rich world, there is a growing need to manage burgeoning amounts of information effectively. Although indexing and linking documents and other information sources is an important step, capturing the knowledge contained within these diverse sources is crucial for the effective use of large information repositories. Knowledge acquisition has been a challenging area of research in artificial intelligence, with its roots in early work to develop expert systems. Driven by the modern Internet culture and by knowledge-based industries, the study of knowledge acquisition has a renewed importance.
</p>
<p>
Although there has been considerable work in the area of knowledge capture, activities have been distributed across several distinct research communities. In machine learning, learning apprentices acquire knowledge by nonintrusively watching a user perform a task. In the human-computer interaction community, programming-by-demonstration systems learn to perform a task by watching a user demonstrate how to accomplish it. In knowledge engineering, modeling techniques and design principles have been proposed for knowledge-based systems, often exploiting commonly occurring domain-independent inference structures and reusable domain-specific ontologies. In planning and process management, mixed-initiative systems acquire knowledge about a user's goals by taking commands or accepting advice regarding a task. In natural language processing, tools can process text and create representations of its knowledge content. All of these approaches are related in that they acquire information and organize it in knowledge structures that can be used for reasoning. They are complementary in that they use different techniques and approaches to capture different forms of knowledge.
</p>
<p>
The aim of K-CAP is to provide a forum in which to bring together disparate research communities whose members are interested in efficiently capturing knowledge from a variety of sources and in creating representations that can be (or eventually can be) useful for reasoning. This conference series promotes multidisciplinary research that could result in a new generation of tools and methodologies for knowledge capture. 
</p>

<h2><a name="history"></a>History and Related Communities</h2> 

<p>
The First International Conference on Knowledge Capture was held in October 21-23, 2001 in Victoria, British Columbia. The conference attracted researchers from diverse areas of AI, including knowledge representation, knowledge acquisition, intelligent user interfaces, problem solving and reasoning, planning, agents, text extraction, and machine learning.
</p>
<p>
A series of Knowledge Acquisition Workshops (KAW) was held from October 1986 every eighteen months until 1999. KAW was always held in Banff (Alberta, Canada) and was hosted by Brian Gaines of the University of Calgary. Brian Gaines retired in 2000, and announced that KAW-99 (its eleventh edition) would be the last one he would host. At that workshop, the KAW community reopened discussions about possibly starting a conference. With Brian Gaines, the co-chairs of the last few editions of KAW were Mark Musen and Rob Kremer and they are both are part of the organizing committee of K-CAP. In addition, a number of people who traditionally attended KAW are part of the program committee, including the KAW steering committee members. We believe that K-CAP is in a good position to attract the KAW community to this new conference. More details on the KAW workshop series can be found at <a href="http://ksi.cpsc.ucalgary.ca/KAW/" rel="external">http://ksi.cpsc.ucalgary.ca/KAW/</a>. A parallel series of European Knowledge Acquisition Workshops commenced with EKAW87 in London, England in September 1987. The EKAW series has continued on an annual basis in various European cities. The 11th EKAW was held at Schloss Dagstuhl in Germany, May 26-29, 1999. Similarly to the KAW series, the EKAW community decided to turn the workshop series into a conference. EKAW'2000, the 12th International Conference on Knowledge Engineering and Knowledge Management, was held October 2-6, 2000 in France (
<a href="http://www-sop.inria.fr/acacia/ekaw2000/" rel="external">http://www-sop.inria.fr/acacia/ekaw2000/</a>). EKAW'2002 was held October 1-4 in Siguenza, Spain (<a href="http://babage.dia.fi.upm.es/ekaw02/ekaw02.htm" rel="external">http://babage.dia.fi.upm.es/ekaw02/ekaw02.htm</a>).  EKAW'2004 was held October 5-8 in Whittlebury Hall, Northamptonshire, UK (<a href="http://kmi.open.ac.uk/events/ekaw/" rel="external">http://kmi.open.ac.uk/events/ekaw/</a>).  EKAW-2006 was held October 2-6 in Podebrady (near Prague), Czech Republic (<a href="http://ekaw.vse.cz/" rel="external">http://ekaw.vse.cz/</a>).
</p>
<p>
Several AAAI Symposia have been organized that are related to the topics of the conference. In 1996, a AAAI Spring Symposium on "Acquisition, Learning, and Demonstration: Automating Tasks for Users", co-chaired by Yolanda Gil, brought together researchers from the knowledge acquisition, machine learning, and intelligent user interfaces communities. In 1997, a AAAI Spring Symposium on Artificial Intelligence in Knowledge Management (<a href="http://ksi.cpsc.ucalgary.ca/AIKM97/" rel="external">http://ksi.cpsc.ucalgary.ca/AIKM97/</a>), co-chaired by Mark Musen and Brian Gaines, brought together knowledge acquisition and knowledge representation researchers interested in acquiring adn managing organizational knowledge. A Fall Symposium on "Learning How to Do Things" was also held in November 2001.
</p>
<p>
In recent years, DARPA has funded several major programs on topics related to Knowledge Capture. The High Performance Knowledge Bases (HPKB) program funded research on knowledge acquisition tools, text extraction, and ontology reuse to support rapic development of large knowledge bases. More recently, the Rapid Knowledge Formation (RKF) program is continuing that work with a focus on tools to enable subject matter experts to author knowledge bases. The Evidence Extraction and Link Discovery (EELD program) funds knowledge representation and machine learning research to derive relational information from factual data. The DARPA/Rome Laboratory Planning Initiative (ARPI) and the more recent Active Templates (AcT) program, funded several projects on user-centered planning and decision aids. The Control of Agent-Based Systems (CoABS) examines relevant topics in agent-based frameworks. The DARPA Agent Markup Language (DAML) program is developing technology to produce semantic markup of Web content. There are many research groups in major research institutions involved in these programs, and the K-CAP conference offers a common forum for presenting their work.
</p>
<p>
A strong interest in these topics is motivated by the existence of the Internet itself. There is a large community of researchers and practitioners interested in the vision of a Semantic Web where Web resources will contain annotations that can be used to reason about their content and offered services. This vision, initially brought forward by the World Wide Web consortium (<a href="http://www.w3.org/DesignIssues/Semantic.html" rel="external">http://www.w3.org/DesignIssues/Semantic.html</a>), is motivating many AI researchers (see <a href="http://www.semanticweb.org" rel="external">http://www.semanticweb.org</a> and <a href="http://www.daml.org" rel="external">http://www.daml.org</a>) to look at the technology that will enable the expression and capture of such annotations. The K-CAP conference provides a forum where this community will be able to share techniques and tools for knowledge authoring and extraction with AI researchers in other areas. 
</p>

